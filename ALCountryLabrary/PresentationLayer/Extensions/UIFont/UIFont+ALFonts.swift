import UIKit
    
extension UIFont {
    
    func alHelveticaNeueThinWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Thin", size: size)!
    }
    
    func alHelveticaNeueRegularWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Regular", size: size)!
    }
    
    func alHelveticaNeueMediumWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Medium", size: size)!
    }
    
    func alHelveticaNeueBoldWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Bold", size: size)!
    }
    
    func alHelveticaNeueLightWithSize(size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Light", size: size)!
    }
        
}
